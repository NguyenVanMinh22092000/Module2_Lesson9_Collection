package set_list_map;

public class Student04 {
    private String name;
    private int id;

    public Student04() {
    }

    public Student04(String name, int id) {
        this.name = name;
        this.id = id;
    }

    @Override
    public String toString() {
        return "Student04{" +
                "name='" + name + '\'' +
                ", id=" + id +
                '}';
    }
}
